    for (int i=11; i<61; ++i)
    {
      if (stageName == EggType[i] && PlayerPrefs.GetString("MyEggList")[i-1]=='0' && Random.Range(0, 100) < EggProb[i])
      {
        result.Add(i);
      }
    }
